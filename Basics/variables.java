public class variables {
    public static void main(String args[]) {
        String name = "Rishu";
        System.out.println(name);
        System.out.println("Your name is " + name); // string concatenation
        int number = 14;
        System.out.println(number);

        float fl = 34.67f;
        System.out.println(fl);

        boolean bool = true;
        System.out.println(bool);

        int myNum = 6;
        myNum = 7;
        System.out.println("Number after updating is : " + myNum);

    }
}